<?php

    /*
    *   OPERADORES DE COMPARAÇÃO 
    */

    $a = 30;
    $b = 55;

    # É maior que #
    var_dump($a > $b);

    # É maior ou igual à/que #
    var_dump($a >= $b);

    # É menor que #
    var_dump($a < $b);

    # É menor ou igual à/que #
    var_dump($a <= $b);

    # Atribuição de Valor #
    var_dump($a = $b);

    # É igual à (Valor) #
    var_dump($a == $b);

    # É identico à #
    var_dump($a === $b);

    # Não são identicos #
    var_dump($a != $b);

    # Não são identicos e nem o tipo dos dados #
    var_dump($a !== $b);


?>
